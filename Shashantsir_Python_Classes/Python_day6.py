# Operators 

#program 1 (Assignment Operators) ---------------
#   [=, +=, -=, *=, /=, //=, %=, **=] 

a = 10 #Value assign in variable.
a += 3  #(Add and Assign Instant)  
print(a)


a = 10 #Value assign in variable.
a -= 3  #(Subtract and Assign Instant)  
print(a)


a = 10 #Value assign in variable.
a *= 3  #(Multiply and Assign Instant)  
print(a)

a = 10 #Value assign in variable.
a /= 3  #(Divide and Assign Instant)  
print(a)

a = 10 #Value assign in variable.
a //= 3  #(Floor Divide and Assign Instant)  
print(a)

a = 10 #Value assign in variable.
a %= 3  #(Modulus Divide and Assign Instant)  (remainder)
print(a)

a = 10 #Value assign in variable.
a **= 3  #(Exponent Divide and Assign Instant)  (Power)
print(a)
