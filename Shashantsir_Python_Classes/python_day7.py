# Operators 

#program 1 (Logical Operators) ---------------
#   [ and, or, not ] 
A = 10
B = 20

print(A < B and A == 10)   # True and True → True
print(A > B and A != B)    # False and True → False

print(A < B or A == 10)    # True or True → True
print(A < B or A > 50)     # True or False → True
print(A > B or A > 50)     # False or False → False

print(not(A < B))   # not(True) → False
print(not(A > B))   # not(False) → True
#program End ---------------


