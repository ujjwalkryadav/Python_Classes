#program 1 (Logical Operators) ---------------
#   [ and, or, not ] 
A = 10
B = 20

print(A < B and A == 10)   # True and True ‚Üí True
print(A > B and A != B)    # False and True ‚Üí False

print(A < B or A == 10)    # True or True ‚Üí True
print(A < B or A > 50)     # True or False ‚Üí True
print(A > B or A > 50)     # False or False ‚Üí False

print(not(A < B))   # not(True) ‚Üí False
print(not(A > B))   # not(False) ‚Üí True
#program End ---------------


